package ch02;

public class Test03 {
  public static void main(String[] args) {
    //12,3456,7890 정수 값을 자릿수를 포함하여 표현해보라.
    // => 3자리 마다 자릿수 표기할 것
    System.out.println(1_234_567_890);
    // => 4자리 마다 자릿수 표기할 것
    System.out.println(12_3456_7890);

    // 0b0110001 2진수 자릿수를 표함하여 표현하라
    // => 4비트 마다 자릿수 표기할 것
    System.out.println(0b011_0001);
    
    //맨 앞과 맨 뒤는 _ 붙이면 안됨
  }
}